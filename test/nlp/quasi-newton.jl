@testset "QuasiNewtonModel NLP tests" begin
  @testset "API" begin
    f(x) = (x[1] - 2)^2 + (x[2] - 1)^2
    ∇f(x) = [2 * (x[1] - 2); 2 * (x[2] - 1)]
    c(x) = [x[1] - 2x[2] + 1; -x[1]^2 / 4 - x[2]^2 + 1]
    J(x) = [1.0 -2.0; -0.5x[1] -2.0x[2]]

    for (QNM, QNO) in [(LSR1Model, LSR1Operator), (LBFGSModel, LBFGSOperator)],
      T in [Float64, Float32],
      M in [NLPModelMeta, SimpleNLPMeta]

      nlp = QNM(SimpleNLPModel(T, M))
      n = nlp.meta.nvar
      m = nlp.meta.ncon

      s, y = randn(T, n), randn(T, n)
      B = QNO(T, n)
      push!(B, s, y)
      push!(nlp, s, y)
      H(x) = B
      H(x, y) = B

      y = randn(T, m)
      x = randn(T, n)
      v = randn(T, n)
      w = randn(T, m)
      Jv = zeros(T, m)
      Jtw = zeros(T, n)
      Hv = zeros(T, n)
      Hvals = zeros(T, nlp.meta.nnzh)

      # Basic methods
      @test obj(nlp, x) ≈ f(x)
      @test grad(nlp, x) ≈ ∇f(x)
      @test hprod(nlp, x, v) ≈ H(x) * v
      @test cons(nlp, x) ≈ c(x)
      @test jac(nlp, x) ≈ J(x)
      @test jprod(nlp, x, v) ≈ J(x) * v
      @test jtprod(nlp, x, w) ≈ J(x)' * w

      # Increasing coverage
      fx, cx = objcons(nlp, x)
      @test fx ≈ f(x)
      @test cx ≈ c(x)
      fx, _ = objcons!(nlp, x, cx)
      @test fx ≈ f(x)
      @test cx ≈ c(x)
      fx, gx = objgrad(nlp, x)
      @test fx ≈ f(x)
      @test gx ≈ ∇f(x)
      fx, _ = objgrad!(nlp, x, gx)
      @test fx ≈ f(x)
      @test gx ≈ ∇f(x)
      @test jprod!(nlp, jac_structure(nlp)..., jac_coord(nlp, x), v, Jv) ≈ J(x) * v
      @test jtprod!(nlp, jac_structure(nlp)..., jac_coord(nlp, x), w, Jtw) ≈ J(x)' * w
      Jop = jac_op!(nlp, x, Jv, Jtw)
      @test Jop * v ≈ J(x) * v
      @test Jop' * w ≈ J(x)' * w
      Jop = jac_op!(nlp, jac_structure(nlp)..., jac_coord(nlp, x), Jv, Jtw)
      @test Jop * v ≈ J(x) * v
      @test Jop' * w ≈ J(x)' * w
      Hop = hess_op(nlp, x)
      @test Hop * v ≈ H(x) * v
      Hop = hess_op!(nlp, x, Hv)
      @test Hop * v ≈ H(x) * v

      reset_data!(nlp)
      Hop = hess_op!(nlp, x, Hv)
      @test Hop * v == v
    end
  end

